            <h1 class="card-title text-white font-weight-bold" style="font-size: 2.2rem;">Manual del Módulo de Presupuesto</h1>
        </div>
        <div class="card-body">
            <h4>Introducción</h4>
            <p>El módulo de Presupuesto es fundamental para la gestión financiera de la institución. Permite controlar la asignación de recursos, monitorear el gasto y realizar los ajustes necesarios para garantizar la correcta ejecución del presupuesto anual.</p>
            <hr>

            <h4>1. Presupuesto</h4>
            <p>Esta sección se centra en la consulta y modificación de las asignaciones presupuestarias.</p>
            <ul>
                <li>
                    <strong>Consultar Presupuesto:</strong>
                    <ol>
                        <li><strong>Visualización General:</strong> Permite ver el estado actual de todas las partidas presupuestarias, incluyendo el monto asignado, el monto comprometido, el monto gastado y la disponibilidad.</li>
                        <li><strong>Búsqueda Específica:</strong> Puede filtrar por código de partida o proyecto para encontrar rápidamente la información que necesita.</li>
                        <li><strong>Herramienta de Consulta:</strong> Es la opción principal para que los usuarios verifiquen la disponibilidad antes de iniciar un proceso de compra o gasto.</li>
                    </ol>
                </li>
                <li>
                    <strong>Créditos Adicionales:</strong>
                    <ol>
                        <li><strong>Incrementar Presupuesto:</strong> Se utiliza para registrar un aumento en una o varias partidas presupuestarias cuando se reciben recursos adicionales no previstos en el presupuesto inicial.</li>
                        <li><strong>Registro y Justificación:</strong> Deberá indicar la partida, el monto del crédito adicional y adjuntar la documentación de soporte que autoriza dicho incremento.</li>
                    </ol>
                </li>
                <li>
                    <strong>Traspasos:</strong>
                    <ol>
                        <li><strong>Movimiento entre Partidas:</strong> Permite mover fondos de una partida presupuestaria a otra. Esto es útil cuando una partida tiene un excedente y otra necesita más recursos.</li>
                        <li><strong>Partida Cedente y Receptora:</strong> Deberá seleccionar la partida que cede los fondos (cedente) y la que los recibe (receptora), así como el monto a traspasar. El sistema valida que la partida cedente tenga disponibilidad suficiente.</li>
                    </ol>
                </li>
            </ul>
            <hr>

            <h4>2. Codificación (Crear)</h4>
            <p>Esta sección permite estructurar el presupuesto, creando las categorías donde se asignarán los fondos.</p>
            <ul>
                <li>
                    <strong>Actividades y Partidas:</strong>
                    <ol>
                        <li><strong>Crear Estructura:</strong> Aquí se crean las nuevas partidas presupuestarias y se asocian a proyectos o actividades específicas.</li>
                        <li><strong>Codificación:</strong> Se asigna un código único a cada partida, siguiendo la estructura contable y presupuestaria de la institución.</li>
                        <li><strong>Uso Restringido:</strong> Generalmente, esta opción solo está disponible para los administradores del sistema o el personal del área de presupuesto.</li>
                    </ol>
                </li>
            </ul>
            <hr>

            <h4>3. Reportes</h4>
            <p>Genera informes consolidados para el análisis de la ejecución presupuestaria.</p>
            <ul>
                <li><strong>Ejecución Presupuestaria:</strong> Genere reportes que muestren un resumen o detalle de cómo se ha ido gastando el presupuesto a lo largo del tiempo.</li>
                <li><strong>Disponibilidad:</strong> Reportes rápidos para conocer el saldo disponible en todas o algunas de las partidas.</li>
            </ul>

