        <div class="card-body">
            <h4>Introducción</h4>
            <p>Este módulo está diseñado para administrar el flujo de documentos y comunicaciones tanto internas como externas de la institución, asegurando un registro y seguimiento adecuado de toda la correspondencia.</p>
            <hr>

            <h4>1. Recibir Correspondencia Externa</h4>
            <p>Gestiona los documentos que ingresan a la institución desde el exterior.</p>
            <ul>
                <li><strong>Gestión:</strong> Registro inicial de la correspondencia recibida. Se anota quién remite, la fecha, el asunto y se digitaliza el documento si es necesario. Se le asigna un número de seguimiento.</li>
                <li><strong>Aprobación:</strong> Dependiendo del tipo de documento, puede requerir la aprobación o visto bueno de una jefatura o departamento antes de ser distribuido.</li>
                <li><strong>Recibir:</strong> El departamento o funcionario destinatario final confirma la recepción del documento, cerrando el ciclo de entrega.</li>
            </ul>
            <hr>

            <h4>2. Enviar Correspondencia Externa</h4>
            <p>Administra los documentos que la institución envía hacia el exterior.</p>
            <ul>
                <li><strong>Gestión:</strong> Se crea el registro del documento a enviar, se adjunta el archivo y se especifican los destinatarios. El sistema genera un número de oficio o comunicación.</li>
            </ul>
            <hr>

            <h4>3. Correspondencia Interna</h4>
            <p>Controla el flujo de memorandos y comunicaciones entre los diferentes departamentos de la institución.</p>
            <ul>
                <li><strong>Recibir Correspondencia Interna:</strong> Bandeja de entrada para los documentos internos dirigidos al usuario o su departamento.</li>
                <li><strong>Enviar Correspondencia Interna:</strong> Permite la creación y envío de comunicaciones a otros usuarios o dependencias dentro de la institución.</li>
            </ul>
            <hr>

            <h4>4. Reportes</h4>
            <p>Genera informes para el seguimiento y control de la correspondencia.</p>
            <ul>
                <li><strong>Reporte de Correspondencia Recibida:</strong> Lista los documentos externos recibidos en un período de tiempo.</li>
                <li><strong>Reporte de Correspondencia Enviada:</strong> Muestra un historial de los documentos enviados.</li>
                <li><strong>Estado de Seguimiento:</strong> Permite consultar el estado actual de un documento específico usando su número de seguimiento.</li>
            </ul>

        </div>
