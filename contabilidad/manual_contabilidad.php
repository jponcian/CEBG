<div class="card-body">
    <h4>Introducción</h4>
    <p>El módulo de Contabilidad es el centro neurálgico del sistema financiero. Se encarga de procesar las retenciones, generar los libros contables y configurar la estructura de cuentas que soporta todas las operaciones.</p>
    <hr>

    <h4>1. Retenciones</h4>
    <p>Esta sección gestiona el cálculo y la declaración de las retenciones de impuestos aplicadas a los pagos.</p>
    <ul>
        <li>
            <strong>Orden de Pago:</strong>
            <ol>
                <li><strong>Aplicar Retenciones:</strong> Una vez que una Orden de Pago es aprobada, en esta opción se calculan y aplican las retenciones de IVA, ISLR, y otras que correspondan según el proveedor y el tipo de servicio o bien.</li>
                <li><strong>Generación de Comprobantes:</strong> El sistema genera automáticamente los comprobantes de retención para ser entregados al proveedor.</li>
            </ol>
        </li>
        <li>
            <strong>Generar TXT y XML:</strong>
            <ol>
                <li><strong>Declaraciones Fiscales:</strong> Esta herramienta permite exportar la información de las retenciones practicadas en un período (generalmente mensual) a los formatos requeridos por la administración tributaria (SENIAT), como son los archivos TXT para el ISLR y XML para el IVA.</li>
            </ol>
        </li>
    </ul>
    <hr>

    <h4>2. Libros</h4>
    <p>Sección dedicada a la generación de los libros contables oficiales.</p>
    <ul>
        <li>
            <strong>Libro Banco:</strong>
            <ol>
                <li><strong>Control de Cuentas Bancarias:</strong> Permite llevar un registro detallado de todos los movimientos (depósitos, transferencias, cheques emitidos) de las cuentas bancarias de la institución.</li>
                <li><strong>Conciliación Bancaria:</strong> Es una herramienta fundamental para realizar la conciliación entre los registros del sistema y los estados de cuenta emitidos por el banco.</li>
            </ol>
        </li>
    </ul>
    <hr>

    <h4>3. Codificación</h4>
    <p>Permite definir y estructurar el plan de cuentas contables de la institución.</p>
    <ul>
        <li>
            <strong>Plan de Cuentas:</strong>
            <ol>
                <li><strong>Crear y Modificar Cuentas:</strong> Aquí se pueden crear nuevas cuentas contables (ej. "Caja Chica", "Bancos Nacionales", "Cuentas por Pagar a Proveedores") o modificar las existentes.</li>
                <li><strong>Estructura Jerárquica:</strong> Se define la jerarquía de las cuentas (cuentas de mayor, subcuentas, etc.), que es la base para todos los registros contables del sistema.</li>
                <li><strong>Acceso Restringido:</strong> Esta es una función crítica y su acceso está limitado al personal de contabilidad autorizado.</li>
            </ol>
        </li>
    </ul>
    <hr>

    <h4>4. Reportes</h4>
    <p>Generación de informes financieros y contables.</p>
    <ul>
        <li><strong>Balance de Comprobación:</strong> Emite un reporte que lista todas las cuentas del libro mayor con sus saldos (débito o crédito), permitiendo verificar que la suma de los débitos sea igual a la suma de los créditos.</li>
        <li><strong>Estado de Resultados y Balance General:</strong> Permite generar los estados financieros básicos de la institución.</li>
    </ul>

