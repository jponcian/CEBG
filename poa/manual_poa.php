        <div class="card-header bg-primary">
            <h1 class="card-title text-white font-weight-bold" style="font-size: 2.2rem;">Manual del Módulo de POAI</h1>
        </div>
        <div class="card-body">
            <h4>Introducción</h4>
            <p>El módulo de Plan Operativo Anual Institucional (POAI) es la herramienta para la planificación, seguimiento y evaluación de los objetivos y metas de la institución. Permite alinear las actividades de los departamentos con la estrategia general.</p>
            <hr>

            <h4>1. Registro</h4>
            <p>Fase inicial donde se define la planificación.</p>
            <ul>
                <li>
                    <strong>Poai & Proyecto:</strong>
                    <ol>
                        <li>Se crean los Proyectos principales que se ejecutarán durante el año, asociándolos a los objetivos estratégicos de la institución.</li>
                        <li>Se define el marco general del POAI, estableciendo el período de vigencia.</li>
                    </ol>
                </li>
                <li>
                    <strong>Metas & Actividades:</strong>
                    <ol>
                        <li>Para cada proyecto, se definen las metas específicas a alcanzar.</li>
                        <li>Se desglosan las metas en actividades concretas, asignando responsables, fechas de cumplimiento y recursos necesarios.</li>
                    </ol>
                </li>
            </ul>
            <hr>

            <h4>2. Gestión</h4>
            <p>Seguimiento del trabajo planificado.</p>
            <ul>
                <li>
                    <strong>Actividades:</strong>
                    <ol>
                        <li>Los responsables registran el avance de las actividades asignadas.</li>
                        <li>Se puede actualizar el estado (iniciada, en progreso, completada), y adjuntar evidencias o productos generados.</li>
                    </ol>
                </li>
            </ul>
            <hr>

            <h4>3. Consultas</h4>
            <p>Visualización del estado de la planificación.</p>
            <ul>
                <li><strong>Poai:</strong> Permite consultar el estado general del POAI, viendo el avance consolidado de sus proyectos y metas.</li>
                <li><strong>Proyectos:</strong> Ofrece una vista detallada de un proyecto específico, con sus metas, actividades y porcentaje de avance.</li>
            </ul>
            <hr>

            <h4>4. Reportes</h4>
            <p>Generación de informes para la toma de decisiones.</p>
            <ul>
                <li><strong>Reporte de POAI:</strong> Genera un informe completo del Plan Operativo, ideal para presentaciones a la dirección.</li>
                <li><strong>Reporte de Proyectos:</strong> Crea un informe detallado sobre el desempeño de uno o varios proyectos.</li>
            </ul>

        </div>
        <div class="card-footer">
            <p>Para más información, contacte al departamento de Tecnología.</p>
