        <div class="card-body">
            <h4>Introducción</h4>
            <p>El módulo de Dirección de Atención y Cultura al Ciudadano (DACC) está orientado a registrar y gestionar las interacciones con el público, ya sea personal, telefónico o por otros medios. Permite llevar un control de las solicitudes, quejas o sugerencias y generar estadísticas sobre la atención brindada.</p>
            <hr>

            <h4>1. Atención</h4>
            <p>Registro de la interacción con los ciudadanos.</p>
            <ul>
                <li>
                    <strong>Personal:</strong>
                    <ol>
                        <li>Permite registrar la atención brindada a una persona, ya sea un funcionario o un visitante.</li>
                        <li>Se anota el motivo de la consulta o trámite, la persona que atiende y la solución o respuesta proporcionada.</li>
                    </ol>
                </li>
            </ul>
            <hr>

            <h4>2. Ajustes</h4>
            <p>Configuración de los catálogos utilizados en el módulo.</p>
            <ul>
                <li>
                    <strong>Motivo Atención:</strong>
                    <ol>
                        <li>Permite crear, modificar o eliminar los diferentes tipos de motivos por los cuales un ciudadano puede contactar a la institución (ej: solicitud de información, reclamo, trámite específico, etc.).</li>
                        <li>Esta parametrización es clave para poder generar reportes estadísticos útiles.</li>
                    </ol>
                </li>
            </ul>
            <hr>

            <h4>3. Reportes</h4>
            <p>Generación de informes para medir la calidad y eficiencia de la atención.</p>
            <ul>
                <li>
                    <strong>Reporte de Atenciones:</strong>
                    <p>Genera un listado detallado de todas las atenciones registradas en un período de tiempo, permitiendo filtrar por tipo de motivo, persona que atendió, etc.</p>
                </li>
                <li>
                    <strong>Estadísticas por Motivo:</strong>
                    <p>Muestra gráficos y datos sobre los motivos de consulta más frecuentes, ayudando a identificar áreas de mejora o necesidades de la comunidad.</p>
                </li>
            </ul>

        </div>
        <div class="card-footer">
