            <h1 class="card-title text-white font-weight-bold" style="font-size: 2.2rem;">Manual del Módulo de Archivo</h1>
        </div>
        <div class="card-body">
            <h4>Introducción</h4>
            <p>El módulo de Archivo permite la gestión y control de los expedientes físicos de la institución, facilitando su registro, ubicación y préstamo.</p>
            <hr>

            <h4>1. Expedientes</h4>
            <p>Sección central para la administración de los expedientes.</p>
            <ul>
                <li>
                    <strong>Registro:</strong>
                    <ol>
                        <li><strong>Creación de Expedientes:</strong> Permite registrar un nuevo expediente en el sistema, asignándole un código único, una descripción y especificando su ubicación física (estante, caja, etc.).</li>
                        <li><strong>Asociar Documentos:</strong> Se pueden detallar los documentos más importantes contenidos en el expediente.</li>
                    </ol>
                </li>
                <li>
                    <strong>Préstamo:</strong>
                    <ol>
                        <li><strong>Solicitud de Préstamo:</strong> Un funcionario puede solicitar el préstamo de un expediente.</li>
                        <li><strong>Registro de Salida:</strong> El personal de archivo registra la salida del expediente, indicando qué funcionario lo retira y la fecha del préstamo.</li>
                        <li><strong>Devolución:</strong> Cuando el expediente es devuelto, se registra su reingreso al archivo, quedando disponible nuevamente.</li>
                    </ol>
                </li>
            </ul>
            <hr>

            <h4>2. Reportes</h4>
            <p>Informes para el control de los expedientes.</p>
            <ul>
                <li><strong>Inventario de Expedientes:</strong> Lista todos los expedientes registrados en el archivo.</li>
                <li><strong>Expedientes en Préstamo:</strong> Muestra un listado de todos los expedientes que se encuentran actualmente en préstamo y quién los tiene.</li>
            </ul>

        </div>
        <div class="card-footer">
            <p>Para más información, contacte al departamento de Tecnología.</p>
        </div>
