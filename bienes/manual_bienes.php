            <h1 class="card-title text-white font-weight-bold" style="font-size: 2.2rem;">Manual del Módulo de Bienes Nacionales</h1>
        </div>
        <div class="card-body">
            <h4>Introducción</h4>
            <p>El módulo de Bienes Nacionales es el encargado de registrar, controlar y gestionar todos los activos fijos de la institución. Su correcta utilización es vital para el resguardo del patrimonio público.</p>
            <hr>

            <h4>1. Bienes Nacionales</h4>
            <p>Esta sección es el núcleo del módulo, donde se registran y gestionan los bienes.</p>
            <ul>
                <li>
                    <strong>Registro:</strong>
                    <ol>
                        <li><strong>Incorporación de Activos:</strong> Aquí se crean las fichas de cada bien adquirido por la institución. Se debe registrar información clave como descripción, marca, modelo, serial, costo de adquisición y fecha de compra.</li>
                        <li><strong>Asignación de Código:</strong> El sistema genera un código único de bien nacional que deberá ser adherido físicamente al activo para su identificación.</li>
                        <li><strong>Asignación Inicial:</strong> Al registrar un bien, se le asigna a un responsable (funcionario) y una ubicación física (dependencia).</li>
                    </ol>
                </li>
                <li>
                    <strong>Inventariar:</strong>
                    <ol>
                        <li><strong>Toma Física de Inventario:</strong> Esta herramienta facilita el proceso de inventario físico. Permite a los usuarios verificar los bienes asignados a su cargo, confirmando su existencia y estado.</li>
                        <li><strong>Actualización de Estado:</strong> Durante el inventario, se puede actualizar el estado de un bien (ej. "En uso", "En reparación", "Deteriorado").</li>
                    </ol>
                </li>
            </ul>
            <hr>

            <h4>2. Movimientos</h4>
            <p>Gestiona los cambios de ubicación o responsable de los bienes ya registrados.</p>
            <ul>
                <li>
                    <strong>Reasignaciones:</strong>
                    <ol>
                        <li><strong>Transferencia de Bienes:</strong> Se utiliza cuando un bien se mueve de una dependencia a otra o cambia de responsable.</li>
                        <li><strong>Generar Acta de Movimiento:</strong> El sistema genera un acta de reasignación que debe ser firmada por el responsable que entrega y el que recibe, formalizando la transferencia de custodia.</li>
                    </ol>
                </li>
            </ul>
            <hr>

            <h4>3. Desincorporaciones</h4>
            <p>Proceso para dar de baja a un bien del inventario de activos.</p>
            <ul>
                <li>
                    <strong>Solicitud de Desincorporación:</strong>
                    <ol>
                        <li><strong>Inicio del Proceso:</strong> Cuando un bien llega al final de su vida útil, está obsoleto o dañado irreparablemente, se inicia una solicitud de desincorporación.</li>
                        <li><strong>Informe Técnico:</strong> Se debe adjuntar un informe técnico que justifique la razón de la baja.</li>
                        <li><strong>Aprobación:</strong> La solicitud pasa por un flujo de aprobación antes de que el bien sea retirado oficialmente del inventario.</li>
                    </ol>
                </li>
            </ul>
            <hr>

            <h4>4. Reportes</h4>
            <p>Genera informes detallados para el control y la auditoría de los bienes.</p>
            <ul>
                <li><strong>Inventario General:</strong> Reporte con el listado completo de todos los bienes de la institución.</li>
                <li><strong>Inventario por Responsable:</strong> Muestra todos los bienes asignados a un funcionario específico.</li>
                <li><strong>Inventario por Dependencia:</strong> Lista todos los bienes ubicados en una oficina o departamento.</li>
                <li><strong>Historial de Movimientos:</strong> Permite rastrear todos los movimientos que ha tenido un bien a lo largo de su historia en la institución.</li>
            </ul>

