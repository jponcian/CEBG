            <h1 class="card-title text-white font-weight-bold" style="font-size: 2.2rem;">Manual del Módulo de Talento Humano (TH)</h1>
        </div>
        <div class="card-body">
            <h4>Introducción</h4>
            <p>El módulo de Talento Humano (TH) es el centro de gestión de todo lo relacionado con el personal de la institución. Abarca desde el proceso de nómina y pagos, hasta la administración de vacaciones, evaluaciones y el registro de la información personal y laboral de cada funcionario.</p>
            <hr>

            <h4>1. Nómina</h4>
            <p>Procesa los pagos regulares de salarios a los empleados.</p>
            <ul>
                <li><strong>Generar Pago:</strong> Inicia el proceso de cálculo de la nómina para un período específico (quincenal, mensual).</li>
                <li><strong>Generar Aguinaldos:</strong> Proceso especial para el cálculo y pago de aguinaldos o bonificaciones de fin de año.</li>
                <li><strong>Ajustes:</strong> Permite ingresar ajustes manuales a la nómina, como bonificaciones extraordinarias, deducciones o correcciones.</li>
                <li><strong>Gestión:</strong> Administra el estado de la nómina generada, permitiendo su aprobación, envío al banco y cierre.</li>
            </ul>
            <hr>

            <h4>2. Pagos Adicionales</h4>
            <p>Gestiona pagos fuera de la nómina regular.</p>
            <ul>
                <li><strong>Horas Extra:</strong> Registro y cálculo del pago por horas extra trabajadas.</li>
                <li><strong>Viáticos:</strong> Integración con el módulo de viáticos para procesar los pagos correspondientes.</li>
                <li><strong>Otros Pagos:</strong> Módulo para registrar cualquier otro tipo de pago o beneficio no contemplado en las otras categorías.</li>
            </ul>
            <hr>

            <h4>3. Vacaciones</h4>
            <p>Administra el ciclo de vacaciones de los funcionarios.</p>
            <ul>
                <li><strong>Calcular Periodo:</strong> El sistema calcula y asigna los días de vacaciones a los que cada funcionario tiene derecho.</li>
                <li><strong>Solicitud:</strong> El funcionario puede solicitar el disfrute de sus vacaciones a través del sistema.</li>
                <li><strong>Aprobar:</strong> La jefatura aprueba o rechaza las solicitudes de vacaciones.</li>
                <li><strong>Reportes:</strong> Informes sobre saldos de vacaciones, solicitudes y planificaciones.</li>
            </ul>
            <hr>

            <h4>4. Funcionarios</h4>
            <p>Directorio y gestión de la información del personal.</p>
            <ul>
                <li><strong>Listado:</strong> Muestra el listado completo de funcionarios activos e inactivos.</li>
                <li><strong>Ficha Personal:</strong> Permite crear, ver y actualizar la ficha completa de cada funcionario (datos personales, familiares, académicos, laborales, etc.).</li>
            </ul>
            <hr>

            <h4>5. Evaluaciones</h4>
            <p>Módulo para la gestión del desempeño.</p>
            <ul>
                <li><strong>Evaluación:</strong> Permite realizar y registrar las evaluaciones de desempeño de los funcionarios.</li>
                <li><strong>Consultas:</strong> Consulta de los resultados históricos de las evaluaciones.</li>
            </ul>
            <hr>

            <h4>6. Reportes</h4>
            <p>Genera una variedad de informes clave para la gestión de Talento Humano.</p>
            <ul>
                <li><strong>Reporte de Nómina:</strong> Informes detallados de las nóminas procesadas.</li>
                <li><strong>Constancias:</strong> Generación de constancias de trabajo y salario para los funcionarios.</li>
                <li><strong>Acciones de Personal:</strong> Registro y consulta de acciones de personal como ascensos, traslados o amonestaciones.</li>
                <li><strong>Carnet:</strong> Emisión de carnets de identificación.</li>
            </ul>

