            <h1 class="card-title text-white font-weight-bold" style="font-size: 2.2rem;">Manual del Módulo de Almacén</h1>
        </div>
        <div class="card-body">
            <h4>Introducción</h4>
            <p>El módulo de Almacén controla el inventario de materiales y suministros de oficina. Gestiona el ciclo completo desde el ingreso de productos hasta su despacho a las diferentes dependencias.</p>
            <hr>

            <h4>1. Solicitudes</h4>
            <p>Flujo de solicitud y entrega de materiales de oficina a los usuarios.</p>
            <ul>
                <li>
                    <strong>Solicitar:</strong>
                    <ol>
                        <li><strong>Crear Solicitud:</strong> Cualquier funcionario puede crear una solicitud de materiales. Deberá seleccionar los artículos del catálogo de almacén y especificar la cantidad requerida de cada uno.</li>
                        <li><strong>Envío para Aprobación:</strong> Una vez completada, la solicitud se envía al jefe de la unidad o al responsable de autorizar.</li>
                    </ol>
                </li>
                <li>
                    <strong>Autorizar:</strong>
                    <ol>
                        <li><strong>Revisión de Solicitudes:</strong> Los jefes de dependencia o supervisores revisan las solicitudes pendientes de su personal.</li>
                        <li><strong>Aprobar o Rechazar:</strong> Pueden aprobar la solicitud (total o parcialmente) o rechazarla. Si se aprueba, pasa al personal de almacén para su preparación.</li>
                    </ol>
                </li>
                <li>
                    <strong>Despachar:</strong>
                    <ol>
                        <li><strong>Preparar Pedido:</strong> El personal de almacén ve las solicitudes autorizadas y prepara los materiales.</li>
                        <li><strong>Registro de Salida:</strong> Al entregar los materiales al solicitante, se registra la salida en el sistema. Esto descuenta automáticamente las cantidades del inventario.</li>
                        <li><strong>Comprobante de Entrega:</strong> Se genera un comprobante que debe ser firmado por quien retira los materiales.</li>
                    </ol>
                </li>
            </ul>
            <hr>

            <h4>2. Inventario</h4>
            <p>Gestión de los productos que se encuentran físicamente en el almacén.</p>
            <ul>
                <li>
                    <strong>Ingresos:</strong>
                    <ol>
                        <li><strong>Registrar Compras:</strong> Cuando se realiza una compra de nuevos suministros, se registran en esta sección.</li>
                        <li><strong>Aumentar Stock:</strong> Se debe indicar el producto, la cantidad ingresada, el costo y el número de orden de compra asociada. El sistema sumará las cantidades al stock disponible.</li>
                    </ol>
                </li>
                <li>
                    <strong>Catálogo de Artículos:</strong>
                    <ol>
                        <li><strong>Crear y Editar Productos:</strong> Permite crear nuevos artículos en el catálogo de almacén (ej. "Resma de papel tamaño carta", "Bolígrafo tinta negra") o editar la información de los existentes.</li>
                    </ol>
                </li>
            </ul>
            <hr>

            <h4>3. Reportes</h4>
            <p>Informes para el control y la gestión del inventario.</p>
            <ul>
                <li><strong>Stock Actual:</strong> Muestra las cantidades disponibles de todos los artículos en el almacén.</li>
                <li><strong>Movimientos de Inventario (Kardex):</strong> Reporte detallado que muestra todas las entradas y salidas de un artículo específico durante un período, permitiendo una trazabilidad completa.</li>
                <li><strong>Consumo por Dependencia:</strong> Informe que totaliza los materiales consumidos por cada departamento, útil para controlar el gasto.</li>
            </ul>

