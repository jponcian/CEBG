        <div class="card-body">
            <h4>Introducción</h4>
            <p>El módulo de Seguridad se encarga de controlar el acceso físico a las instalaciones, registrando tanto la asistencia del personal como las visitas externas. También proporciona herramientas para la comunicación y la generación de reportes de control.</p>
            <hr>

            <h4>1. Asistencia y Visitas</h4>
            <p>Control de entrada y salida de personas a la institución.</p>
            <ul>
                <li>
                    <strong>Gestión:</strong>
                    <ol>
                        <li><strong>Registro de Asistencia:</strong> Permite registrar la hora de entrada y salida de los funcionarios.</li>
                        <li><strong>Registro de Visitas:</strong> Se registran los datos de las personas externas que visitan la institución, a quién visitan, el motivo y la hora de entrada/salida.</li>
                    </ol>
                </li>
                <li>
                    <strong>Teléfono:</strong>
                    <ol>
                        <li>Directorio telefónico interno para consulta rápida por parte del personal de seguridad.</li>
                    </ol>
                </li>
            </ul>
            <hr>

            <h4>2. Reportes</h4>
            <p>Informes para el análisis de la información de seguridad.</p>
            <ul>
                <li>
                    <strong>Asistencia (Estadística):</strong>
                    <p>Genera gráficos y estadísticas sobre la puntualidad y ausentismo del personal.</p>
                </li>
                <li>
                    <strong>Asistencia (Detalle):</strong>
                    <p>Proporciona un reporte detallado con las horas exactas de entrada y salida de los funcionarios en un rango de fechas.</p>
                </li>
                <li>
                    <strong>Visitas:</strong>
                    <p>Genera un listado de las visitas recibidas, permitiendo filtrar por fecha, visitante o persona visitada.</p>
                </li>
            </ul>

        </div>
