            <h1 class="card-title text-white font-weight-bold" style="font-size: 2.2rem;">Manual del Módulo de Viáticos</h1>
        </div>
        <div class="card-body">
            <h4>Introducción</h4>
            <p>El módulo de Viáticos gestiona todo el ciclo de vida de las solicitudes de viáticos por parte de los funcionarios, desde la solicitud inicial hasta la rendición de cuentas, pasando por las aprobaciones correspondientes.</p>
            <hr>

            <h4>1. Funcionario</h4>
            <p>Acciones que puede realizar el funcionario que solicita el viático.</p>
            <ul>
                <li>
                    <strong>Solicitud:</strong>
                    <ol>
                        <li>Permite al funcionario crear una nueva solicitud de viático.</li>
                        <li>Debe especificar el motivo del viaje, destino, fechas de salida y retorno, y un estimado de los gastos.</li>
                        <li>Una vez creada, la solicitud pasa al siguiente nivel para su aprobación.</li>
                    </ol>
                </li>
                <li>
                    <strong>Rendición:</strong>
                    <ol>
                        <li>Después de realizado el viaje y recibido el dinero, el funcionario debe realizar la rendición de cuentas.</li>
                        <li>En esta sección, se adjuntan las facturas y soportes de los gastos efectuados.</li>
                        <li>Se compara lo gastado con el monto asignado y se calcula el balance.</li>
                    </ol>
                </li>
            </ul>
            <hr>

            <h4>2. Coordinador</h4>
            <p>Acciones que realiza el coordinador o jefe de la unidad solicitante.</p>
            <ul>
                <li>
                    <strong>Aprobar:</strong>
                    <ol>
                        <li>El coordinador revisa las solicitudes de viáticos de su personal.</li>
                        <li>Puede aprobar la solicitud, enviándola al siguiente nivel (usualmente administrativo o financiero), o rechazarla con una justificación.</li>
                    </ol>
                </li>
                <li>
                    <strong>Enviar:</strong>
                    <ol>
                        <li>Una vez que las solicitudes están aprobadas por la coordinación, esta opción permite agruparlas y enviarlas formalmente al departamento encargado de la gestión y pago de los viáticos.</li>
                    </ol>
                </li>
            </ul>
            <hr>

            <h4>3. Reportes</h4>
            <p>Esta sección (no visible en el menú pero implícita en el flujo) permite generar informes sobre el estado de las solicitudes, viáticos pagados, y rendiciones pendientes para un control administrativo y financiero.</p>

        </div>
        <div class="card-footer">
            <p>Para más información, contacte al departamento de Tecnología.</p>
        </div>
