        <div class="card-body">
            <h4>Introducción</h4>
            <p>El módulo de Tecnología es una herramienta administrativa para la gestión de los usuarios del sistema. Permite controlar quién tiene acceso, qué permisos posee y realizar otras tareas de administración relacionadas con las cuentas de usuario.</p>
            <hr>

            <h4>1. Gestión</h4>
            <p>Administración de las cuentas de acceso al sistema.</p>
            <ul>
                <li>
                    <strong>Usuarios:</strong>
                    <ol>
                        <li><strong>Creación y Edición de Usuarios:</strong> Permite dar de alta a nuevos usuarios en el sistema, asignándoles un nombre de usuario y una contraseña inicial.</li>
                        <li><strong>Asignación de Permisos:</strong> Se definen los módulos y las opciones específicas a las que cada usuario tendrá acceso. Esto se hace asignando perfiles o permisos individuales.</li>
                        <li><strong>Bloqueo y Desbloqueo:</strong> Permite desactivar temporalmente la cuenta de un usuario sin eliminarla.</li>
                    </ol>
                </li>
                <li>
                    <strong>Jefatura:</strong>
                    <ol>
                        <li><strong>Aprobación de Accesos:</strong> En algunos flujos, la creación o modificación de permisos para un usuario puede requerir la aprobación de una jefatura. Esta opción permite a los jefes de departamento gestionar dichas solicitudes.</li>
                        <li><strong>Revisión de Permisos de Equipo:</strong> Permite a un jefe de área consultar los permisos que tienen asignados los miembros de su equipo.</li>
                    </ol>
                </li>
            </ul>
            <hr>

            <h4>2. Auditoría (Implícito)</h4>
            <p>Aunque no visible en el menú, este módulo suele incluir una sección de auditoría que permite a los administradores rastrear las acciones importantes realizadas por los usuarios en el sistema, como inicios de sesión, cambios de datos críticos, etc.</p>

